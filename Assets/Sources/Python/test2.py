    def analyze_user_sentiment(self, message):
        message_lower = message.lower()
        happy_words = ['—Ä–∞–¥', '—Å—á–∞—Å—Ç–ª–∏–≤', '—Ö–æ—Ä–æ—à–æ', '–æ—Ç–ª–∏—á–Ω–æ', '—É—Ä–∞']
        sad_words = ['–≥—Ä—É—Å—Ç–Ω–æ', '–ø–ª–æ—Ö–æ', '—É—Å—Ç–∞–ª', '–±–µ—Å–∏—Ç']

        happy_count = sum(1 for word in happy_words if word in message_lower)
        sad_count = sum(1 for word in sad_words if word in message_lower)

        if happy_count > sad_count:
            return "happy"
        elif sad_count > happy_count:
            return "sad"
        else:
            return "neutral"

    def update_mood(self, user_sentiment):
        mood_changes = {
            'happy': 0.1,
            'sad': -0.1,
            'angry': -0.2,
            'neutral': 0.0
        }

        self.mood += mood_changes.get(user_sentiment, 0.0)
        self.mood = max(0.1, min(1.0, self.mood))

        self.energy -= 0.02
        if user_sentiment == 'happy':
            self.energy += 0.1
        self.energy = max(0.3, min(1.0, self.energy))

    def get_emotional_response(self, category):
        responses = self.response_rules.get(category, self.response_rules['default'])
        personality_responses = responses.get(self.personality, responses['friendly'])

        if self.mood < 0.3:
            return choice(personality_responses)[:50] + "..."
        elif self.mood > 0.8:
            return choice(personality_responses) + " " + choice(["üéâ", "üòä", "üåü"])
        else:
            return choice(personality_responses)

